package net.sf.gridarta.model.gameobject;

/**
 * Provides information about isometric map squares.
 * @author Andreas Kirschbaum
 */
public class IsoMapSquareInfo {

    /**
     * The horizontal size of a square.
     */
    private final int xLen;

    /**
     * The horizontal center of a square.
     */
    private final int xLen2;

    /**
     * The vertical size of a square.
     */
    private final int yLen;

    /**
     * The vertical center of a square.
     */
    private final int yLen2;

    /**
     * Creates a new instance.
     * @param xLen the horizontal size of a square
     * @param xLen2 the horizontal center of a square
     * @param yLen the vertical size of a square
     * @param yLen2 the vertical center of a square
     */
    public IsoMapSquareInfo(final int xLen, final int xLen2, final int yLen, final int yLen2) {
        this.xLen = xLen;
        this.xLen2 = xLen2;
        this.yLen = yLen;
        this.yLen2 = yLen2;
    }

    /**
     * Returns the horizontal size of a square.
     * @return the horizontal size
     */
    public int getXLen() {
        return xLen;
    }

    /**
     * Returns the horizontal center of a square.
     * @return the horizontal center
     */
    public int getXLen2() {
        return xLen2;
    }

    /**
     * Returns the vertical size of a square.
     * @return the vertical size
     */
    public int getYLen() {
        return yLen;
    }

    /**
     * Returns the vertical center of a square.
     * @return the vertical center
     */
    public int getYLen2() {
        return yLen2;
    }

}
